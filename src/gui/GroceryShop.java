    private void addProduct() {
        String productName = productNameTextField.getText();
        double productPrice = Double.parseDouble(productPriceTextField.getText());
        double productQty = Double.parseDouble(productQtyTextField.getText());

        DefaultTableModel dtm = (DefaultTableModel) CartTable.getModel();
        Vector vector = new Vector();
        vector.add(productName);
        vector.add(productQty);
        vector.add(productPrice * productQty);
        dtm.addRow(vector);
    }

    private void calculateTotal() {
        double total = 0.0;
        int rowCount = CartTable.getRowCount();
        for (int i = 0; i < rowCount; i++) {
            double totalProductPrice = Double.parseDouble(CartTable.getValueAt(i, 2).toString());
            total = total + totalProductPrice;
        }
        netTotalTextField.setText("LKR " + String.valueOf(total));

    }

