    private void productPriceTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_productPriceTextFieldKeyTyped
        // TODO add your handling code here:
        if (((Character.isDigit(evt.getKeyChar())) | (String.valueOf(evt.getKeyChar()).equals(".")))) {
            if (productPriceTextField.getText().contains(".")) {
                if (!Character.isDigit(evt.getKeyChar())) {
                    evt.consume();
                }
            }
        } else {
            evt.consume();
        }
    }//GEN-LAST:event_productPriceTextFieldKeyTyped

